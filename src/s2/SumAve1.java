public class SumAve1 {

    public static void main(String[] args) {
        int x = 63;
        int y = 18;

        System.out.println("x의 값은 " + x + " 입니다.");
        System.out.println("y의 값은 " + y + " 입니다.");

        System.out.println("합계는 " + (x + y) + " 입니다.");
        System.out.println("평균은 " + (x + y) / 2 + " 입니다.");
    }
}
